    void CargarTabla(String valor){
        String sSQL="";
        modelo=new DefaultTableModel(null, titulos);
        conexion mysql=new conexion();
        Connection cn=mysql.conectar();
        sSQL="SELECT * FROM estudiante WHERE id_estudiante LIKE'%" +valor+"%'";
        try{
            Statement st=cn.createStatement();
            ResultSet rs=st.executeQuery(sSQL);
            while(rs.next()){
                datos[0]=rs.getString("id_estudiante");
                datos[1]=rs.getString("nombres");
                datos[2]=rs.getString("apellidos");
                datos[3]=rs.getString("documento");
                modelo.addRow(datos);

            }
            tblEst.setModel(modelo);
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error "+ex, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
   

